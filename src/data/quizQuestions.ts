// Function to calculate quiz score
export const calculateQuizScore = (answers: number[], questions: any[]) => {
  let correct = 0;
  answers.forEach((answer, index) => {
    if (answer === questions[index].correct) {
      correct++;
    }
  });
  return Math.round((correct / questions.length) * 100);
};

